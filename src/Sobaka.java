public class Sobaka {
    private String name;
    private int age;
    private String color;
    private String chtoUmeet;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getChtoUmeet() {
        return chtoUmeet;
    }

    public void setChtoUmeet(String chtoUmeet) {
        this.chtoUmeet = chtoUmeet;
    }
}
